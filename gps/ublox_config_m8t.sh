#!/bin/bash

SPEED=230400

gpsctl -s ${SPEED}

export UBXOPTS="-P 22 -v2"
ubxtool -p RESET
ubxtool -e BINARY
ubxtool -d NMEA
ubxtool -d GLONASS
ubxtool -d BEIDOU
ubxtool -d SBAS
ubxtool -p MODEL,2
ubxtool -p CFG-PMS,0
ubxtool -c 0x06,0x31,0x00,0x01,0x00,0x00,0x19,0x00,0x00,0x00,0x40,0x42,0x0f,0x00,0x40,0x42,0x0f,0x00,0x20,0xa1,0x07,0x00,0x20,0xa1,0x07,0x00,0x00,0x00,0x00,0x00,0x73,0x00,0x00,0x00
ubxtool -e SURVEYIN,3600,500000

echo "check surey with:"
echo "  ubxtool -p TIM-SVIN | grep SVIN -A 2"

echo "save with:"
echo "  ubxtool -p SAVE"
